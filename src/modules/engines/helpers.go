package engines

import (
	"fmt"
	"path/filepath"
	"strings"
)

func AddCommentToGeneratedFile(file string, resourceGenerationMode string, templateGenerationMode string, templateContentStr string) string {
	extension := filepath.Ext(file)
	autoGeneratedInfo := ""
	if extension == ".cs" {
		autoGeneratedInfo = fmt.Sprintf("/*\nPage generated by Pars on generate mode resource: %s, template: %s\n*/\n", resourceGenerationMode, templateGenerationMode)
	} else if extension == ".json" {
	} else if extension == ".ts" {
		autoGeneratedInfo = fmt.Sprintf("/*\nPage generated by Pars on generate mode resource: %s, template: %s\n*/\n", resourceGenerationMode, templateGenerationMode)
	}
	templateContentStr = strings.Join([]string{autoGeneratedInfo, templateContentStr}, "")
	return templateContentStr
}
